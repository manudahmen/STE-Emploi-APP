package steemploi.service.events;

import java.net.URL;

public class JPO {
	private int evt_id;
	private int id;
	private URL url;
	private String table_name;
	public int getEvt_id() {
		return evt_id;
	}
	public void setEvt_id(int evt_id) {
		this.evt_id = evt_id;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public URL getUrl() {
		return url;
	}
	public void setUrl(URL url) {
		this.url = url;
	}
	public String getTable_name() {
		return table_name;
	}
	public void setTable_name(String table_name) {
		this.table_name = table_name;
	}
	
	
}
